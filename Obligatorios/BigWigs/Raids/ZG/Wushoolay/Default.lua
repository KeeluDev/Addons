-- automated test
function module:TestModule()
	module:OnEnable()
	module:OnSetup()
	module:OnEngage()

	module:TestModuleCore()

	-- check event handlers
	module:Event(L["trigger_lightningcloud"])
	module:Event(L["trigger_chainLightning"])
	
	module:OnDisengage()
	module:TestDisable()
end

