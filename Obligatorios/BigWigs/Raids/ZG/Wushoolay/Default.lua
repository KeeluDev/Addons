------------------------------
--      Initialization      --
------------------------------

-- called after module is enabled
function module:OnEnable()
	self:RegisterEvent("CHAT_MSG_SPELL_CREATURE_VS_CREATURE_DAMAGE", "Event")
	self:RegisterEvent("CHAT_MSG_SPELL_PERIODIC_SELF_DAMAGE", "Event")
	
	self:ThrottleSync(3, syncName.chainlightning)
end


------------------------------
--      Event Handlers      --
------------------------------
function module:Event(msg)
	if msg == L["trigger_lightningcloud"] and self.db.profile.lightningcloud then
		self:Message(L["msg_lightningCloud"], "Attention", "Alarm")
	elseif msg == L["trigger_chainLightning"] then
		self:Sync(syncName.chainlightning)
	end
end


